package Practices;

public class DivideByZero {
    public static void main(String[] args) {
        /*
        What will be the output when we divide NUMBER WITH ZERO?
         */

       // System.out.println(2/0);//Exception Occur
        System.out.println(2.9/0);// Infinity
        System.out.println(9.0/0);
        System.out.println(23.44f/0);
        System.out.println(19/0.0);
        System.out.println(19.9999d/0);
        //System.out.println(0/0);
        System.out.println(0.0/0);
        System.out.println(0.0/0.0);
        System.out.println(12.33/0.0);
        System.out.println(0/0.0);

        }
}